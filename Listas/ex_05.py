# Listas
  # Estruturas de dados que armazenam coleções de elementos
  # Manter a sequencia dos dados em uma variável

itens = [['item1', 'item2'], ['item3', 'item4']]

print(itens)
print(itens[0][1])  # Acessa o segundo elemento da primeira lista
print(itens[1][0])  # Acessa o primeiro elemento da segunda lista