# Listas
  # Estruturas de dados que armazenam coleções de elementos
  # Manter a sequencia dos dados em uma variável

cidades = ['São Paulo', 'Rio de Janeiro', 'Belo Horizonte']

cidades.append('Curitiba')  # Adiciona um elemento ao final da lista

print(cidades)

cidades.remove('Rio de Janeiro')  # Remove um elemento específico da lista

print(cidades)

cidades.pop(0)

print(cidades)